package array_sorulari;

public class ArrayTrSoru7 {
    public static void main(String[] args) {

        /*
            Bir diziyi parametre olarak kabul eden ve dizideki tüm öğelerin toplamını döndüren bir yöntem yazın.
            Ardından sonucu ana yöntemde yazdırın. Örneğin : giriş : {1,2,3,4,5,6,7,8}; çıktı: 36
         */



        int arr[]={1,2,3,4,5,6,7,8};

        int sum = 0;

        for ( int w: arr){
            sum = sum+w;
        }
        System.out.println(sum);






































    }
}
