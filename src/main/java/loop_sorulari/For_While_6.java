package loop_sorulari;

public class For_While_6 {
    public static void main(String[] args) {


        // Konsolda Alfabeyi yazdırın. ÇIKTI : a b c .. .. .. y z

        char ch = 'a';

        for (int i ='a'; i <='z' ; i++) {

            System.out.print(ch+" ");
            ch++;

        }

































    }
}
