package loop_sorulari;

public class For_While_10 {
    public static void main(String[] args) {

        // 1'den 100'e kadar olan doğal sayıların toplamını bulan programı yazınız. ÇIKTI : 5050 Doğal Sayıların Toplamı

        int sum = 0;

        for (int i = 100; i >0 ; i--) {

            sum = sum+i;

        }
        System.out.println(sum);





































    }
}
